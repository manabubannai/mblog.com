<h1 class="title">Siddhartha (Hermann Hesse) — Book Review for Burned-Out Entrepreneurs</h1>
<img src="/img/siddhartha-hermann-hesse-book-review.jpg">
<p><a href="https://www.amazon.com/dp/0812416643" target="new">This book</a> reminded me of my early days as an entrepreneur.</p>
<blockquote>
<p>The owner conducted his business with care and often with passion, but Siddhartha looked upon all of this as if it were a game.</p>
</blockquote>
<p>This was the attitude I had when I started my business. There was no fear, no clear path—but I moved forward to maximize my profits.</p>
<ul class="long_list">
<li>At that time, I really loved Twitter and blogging so I started creating content. It wasn’t business—it was my passion and genuine interest.</li>
<li>Suddenly, I realized that I could create better content than the articles ranking first on Google. That’s when my journey began.</li>
</ul>
<p>I created my content with care and passion. I also developed my own writing framework because I needed it for myself.</p>
<p>After achieving tons of #1 Google rankings, I started sharing my strategies, which became <a href="https://youtu.be/7J30apoL3nQ?si=QBrTnsTA--Zm9sxj" target="new">common SEO practices</a> in the Japanese market.</p>
<hr>
<p>Why could I do this? Because I loved the game and enjoyed it without fear. But after achieving success, I changed. Let me share this line: </p>
<blockquote>
<p>I can think, I can wait, I can fast</p>
</blockquote>
<p>This is the recurring phrase in this novel, the word of Siddhartha. After achieving success, my problems surfaced.</p>
<ul class="long_list">
<li><b>I can think:</b> With success, I became afraid of making mistakes. This led me to follow others' strategies making me less original and less interesting.</li>
<li><b>I can wait:</b> Gradually, I became someone who could not wait because my half-million followers were waiting for my next move.</li>
<li><b>I can fast (=Patience):</b> Gradually, I lost my patience. I thirsted for the next big wave and told my followers to ride it—even though I wasn’t sure about it.</li>
</ul>
<p>Lastly, here is the line that struck me:</p>
<blockquote>
<p>He is only thinking of the thing he is seeking, because he is obsessed with his goal. Seeking means: to have a goal; but finding means: to be free.</p>
</blockquote>
<p>I decided to be free again. I stopped everything and returned to a blank slate. That is my learning from this book and my output. Thank you for reading.</p>
<hr>
<p>Here are the quotes I dog-eared:</p>
<blockquote>
<p>He was open to everything. Welcome was the merchant who offered him linen for sale, welcome was the beggar who told him for one hour the story of his poverty. He did not treat the rich foreign merchant any different than the servant who shaved him and the street-vendor whom he let cheat him out of some small change when buying bananas.</p>
</blockquote>
<blockquote>
<p>“When someone is seeking,” said Siddhartha, “it happens quite easily that he only sees the thing that he is seeking; that he is unable to find anything, unable to absorb anything, because he is only thinking of the thing he is seeking, because he has a goal, because he is obsessed with his goal. Seeking means: to have a goal; but finding means: to be free, to be receptive, to have no goal. You, O worthy one, are perhaps indeed a seeker, for in striving towards your goal, you do not see many things that are under your nose.“</p>
</blockquote>



