<h2 id="01">１．頭の疲労感をなくし、日々快適に作業する方法</h2>
<p>結論：無駄な思考を排除する。</p>
<p>時代が大きく変化する時代。多くの人がAIに無限相談しており、そして脳は考えるだけで「体験」をします。つまり脳が無限行動で疲れている。だから考えなければ解決。</p>
<ul>
<li>でも考えないと未来が暗い？ いえ、考え過ぎの方が問題です。</li>
</ul>
<p>AIは過去をベースに回答しており、これから世界の8割が変化するのに、過去軸で考えても何も見えません。じゃあどうするか？ 諦めつつ楽しむだけ。</p>
<ul>
<li>成功しないかもだけど日々を楽しむ。人間の向かう先は死だけ。あとは余興です。</li>
</ul>
<p>だから過程を楽しむしかないし、考えるだけで疲れていたら、明るくなるはずの未来も暗くなる。そこで今回は「瞑想的な生き方」を書きます。</p>
<h2 id="02">２．日々を瞑想的に生き、夜でも快適に読書する方法</h2>
<p>結論：Don’t seek it, but find it (探すじゃなく見つける) 。</p>
<p>脳を「探し続けるモード」から「発見モード」に切り替えます。繰り返しですが未来は大幅に変化する。それなのに過去軸で考えても壁に当たるだけ。</p>
<ul>
<li>AIに相談したら「答えっぽい内容」が返ってきます。</li>
<li>しかしAIなんて、上位記事の「総合情報」です。</li>
</ul>
<p>Google検索より良いけど、しかし知らないブロガーに人生相談しますか？ 多くの人がこれをやっています。変化したいなら思考を変えるべき。じゃあどうするか？</p>
