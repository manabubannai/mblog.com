<h3>その①：雑念が出たらDon’t Seekを唱える</h3>
<p>ベストな方法じゃないけど、雑念が湧いたら「Don’t Seek（探さない）」を唱えます。お坊さんのお経と同じ。唱えることで思考を減らせる。</p>
<p>考えることが習慣化してると、すぐに思考は止まりません。依存症と同じ。脳はそれが良いと勘違いしおり、そして無限思考で疲れ、失敗率が上がります。</p>
<ul>
<li>世界が大きく変化する時代の鬱や不安は、新しい未来を受け入れる期間です。</li>
</ul>
<p>あと、このような文章を定期でも読むのも大切。脳は常に「今までの状態」に戻ろうとする。しかし本や記事で正しい思考を取り入れ、徐々に調教しましょう。</p>
<p><i>※僕は継続でこうった発信をするので、もし良ければ<a href="https://mblog.substack.com/welcome" target="new">メルマガ登録</a>をどうぞ📝</i></p>
<h3>その②：日常に「Side Quest」を取り入れる</h3>
<p>ルーティンに「変化する要素」を取り入れる。歌手の<a href="https://youtube.com/shorts/skXZVOi1gxc?si=iOlVuF2CiAgyVDu2" target="new">Ed Sheeran</a>も実行しています。</p>
<ul><li>まず人生の本業を「Quest（クエスト）」と考えます。モンハンみたいな感じ。</li>
<li>でも無限に狩りに出たら消耗するし、たまには友人と遊びたいし、BBQするも良い。</li></ul>
<p>つまりSide Questとは「重要じゃないけど、人生を豊かにするタスク」です。これは「<a href="https://www.amazon.co.jp/dp/1847943756" target="new">Feel-Good-Productivity</a>」の本で紹介されています。</p>
<p>そしてSide Questは「心が最も開いている状態」で実行すると良い。なぜなら残業上がりでSide Questを考えても、秒で帰宅くらいしか出ません。</p>
<p class="pre">僕は瞑想後に実行しており、本日の内容は「①チャリ移動で歌う、②店員に手を合わせて感謝する、③瞑想寺のようなアプリ開発を考える」です。</p>
<p>そしてモンハンと同じで、Side Questは達成しなくてもOK。忘れてもOK。ルーティンに組み込むことで、日常に変化が起きます。</p>
<h3>その③：1日10分でもいいので、瞑想をする</h3>
<p>２つを紹介します : 4-4-6-2呼吸法とBreath Walk。</p>
<h4>4-4-6-2呼吸法とは</h4>
<p>名前の通りで、４秒吸って、４秒止めて、６秒吐いて、２秒止める。これを繰り返すだけ。</p>
<ul>
<li>人間には「交感神経」と「副交感神経」がある</li>
<li>交感神経は「行動エネルギー : HP」を担当する</li>
<li>副交感神経「回復エネルギー : MP」を担当する</li>
</ul>
<p>以前の僕は、10年ほど不眠症でした。なぜなら体が常に「行動モード」で交感神経が高かった。4-4-6-2呼吸法は、副交感神経をアクティブにします。</p>
<p class="pre"><a href="https://pacedbreathing.app/" target="new">Paced Breathing</a>というアプリでリズム設定できるので、それに合わせて呼吸するだけ。これが瞑想の第一歩です。</p>
<p>なお雑念の中で4-4-6-2呼吸しても効果あり。そして言うまでもなく、雑念を減らせたら効果もアップ。朝の10分で4-4-6-2呼吸するのも良いです。</p>
<h4>Breath Walkとは</h4>
<p>世界的ベストセラー書籍の「<a href="https://www.amazon.co.jp/dp/0593420217" target="new">Breath</a>」の内容。</p>
<ul>
<li>方法は簡単で、まずは歩きながら自然に呼吸をする。</li>
<li>適当なタイミングで息を吐いてから、そして止める。</li>
<li>可能な限り息を止めて歩く。この歩数を増やすだけ。</li>
</ul>
<p><i>※注意点：苦しくなるほど息を止める必要なし。イメージとしては「呼吸を再開したら、２〜３回の呼吸で通常に戻る」くらいでOKです。</i></p>
<p>意味あるの？ はい。現代人の多くはストレスや緊張で「過剰に呼吸」をしています。酸素を過剰に吸っており、Breath Walkで二酸化炭素を増やせます。</p>
<p class="pre">二酸化炭素は悪いものと思われるけど、そうじゃない。体内に二酸化炭素があることで、細胞に酸素が届きやすくなる。そして全身の緊張もほぐれます。</p>
<p>例えば朝に10分の瞑想をするなら、その前に軽くBreath Walkしつつ散歩するなど。僕のルーティンです。もしくは通勤中でも、チャリ移動でも実行できる🙆‍♂️</p>
