① 言葉の信頼が、失墜した

ユヴァル・ノア・ハラリがダボス会議でこう語ってました。

「法律、宗教、国家、お金。これらはすべて人間の言語で作られている。AIが人間以上に説得力のある物語を書き始めた今、社会のOSが人間からAIへと移り変わった」

つまり、言葉だけで成り立ってる世界は、AIが支配する。

身近な例で言うと。

上司があなたにメールを送る。丁寧で、的確で、あなたのことをよく理解してる文面。

でもそれ、AIが書いてます。

あなたの業務ログを読ませて、「この部下に最も刺さる言葉」をAIが生成してる。1人のマネージャーが、AIを使って無限に部下を管理できる時代。

法律もAIが書く。契約書もAIが書く。メールもAIが書く。

言葉がAIに書かれた瞬間、「言葉の信頼」は消える。

じゃあ何が残るか。行動です。実際に会うこと。一緒に時間を過ごすこと。画面越しじゃない、リアルの信頼。

---

② スキルの定義が変わった

昔のスキルは、人間が頑張って学んで身につけるものだった。

今は違います。

AIに「ステップバイステップのガイド」を渡すこと。それが新しいスキル。

AIは一瞬でステップを学ぶ。学習時間がゼロ。

「人間のスキル = 学習時間の投資」から、
「AIのスキル = プロンプト1枚」に変わった。

実際に僕がやってます。AIに栄養学のプロンプトを1枚渡しただけで、毎日の食事を分析してくれる。人間が栄養士の資格を取るのに2年かかることを、AIは1秒で学ぶ。

昨日、友人と話してたんですが、彼の彼女がagodaに転職しようとしてると。

正直に言うと、そこで学んだスキルは2〜3年で使い物にならなくなると思う。急速にAI進化と自動化が進んでるから。

彼女は綺麗な人なので、それなら自分の外見を活かせる領域、つまり人と対面で会う仕事を選ぶ方が、長期的にスキル資産や人脈資産を残せると思う。

PCスキルは2年で陳腐化する。でも「信頼される人柄」と「会いたいと思わせる存在感」は陳腐化しない。

じゃあ人間は何を学ぶべきか。

「AIに何をさせるか」を考える力。つまり意思決定。
そして、AIにできないこと。体力、信頼、リアルの人間関係。

全部つながってます。

