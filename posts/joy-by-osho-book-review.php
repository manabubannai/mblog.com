<p>I made five million dollars in five years, but I was unhappy. </p>
<p>After success, I constantly tried to do new things, because that is what every entrepreneur does. Then I found <a href="https://www.amazon.com/dp/031253857X" target="new">this line</a>:</p>
<blockquote>
<p>Please look at what you are doing, what nonsense you are doing. First you create a problem, then you go in search of a solution. Just exactly in the beginning, when you are creating the problem, is the solution — don&#39;t create it!</p>
</blockquote>
<p>This is it. I had to accept what I had—the money, the success, the person I&#39;d become. If I didn&#39;t accept my wealth, I couldn&#39;t create the right strategy for business and life.</p>
<hr>
<p>I fell into depression. Maybe I just needed to rest. But I couldn&#39;t. I had half a million followers waiting for my next move. Then OSHO said:</p>
<blockquote>
<p>If you want to drop the misery, you will have to drop those investments too. If you talk about your misery to people, they give sympathy to you. If you love getting sympathy from people; that is your investment.</p>
</blockquote>
<p>I dropped my misery, and now I&#39;m okay. </p>
<p>Getting rid of misery is not easy. I did it through meditation—one hour every morning, first thing. It took two years, but it made me a different person.</p>
<hr>
<p>Thats it. Below are some other highlights:</p>
<blockquote>
<p>You want to forget and forgive, because the only way to forget is to forgive-if you do not forgive you cannot forget.</p>
</blockquote>
<blockquote>
<p>My understanding is this: that if even one percent of humanity becomes meditative, wars will disappear. And there is no other way. If one percent of humanity — that means one in a hundred people — becomes meditative, things will have a totally different arrangement. Greed will be less; naturally, poverty will be less. Poverty does not exist because things are scarce; poverty exists because people are are greedy. If we live right now, there is enough; the earth has enough to give us.</p>
</blockquote>
<blockquote>
<p>Whenever some body else is responsible for your misery, you are not aware that by giving the responsibility away, you are losing your freedom. Responsibility and freedom are two sides of the same coins.</p>
</blockquote>
<blockquote>
<p>In dictionaries, loneliness and aloneness are synonymous — in life they are not. Loneliness is a state of mind when you are constantly missing the other. Aloneness is the state of mind when you are constantly delighted in yourself. Loneliness is miserable. Aloneness is blissful. </p>
</blockquote>
<blockquote>
<p>In India we call life &quot;the wheel.&quot; It goes on moving, goes on repeating itself. As far as I can see, unless the one became a buddha, the story would have been repeated again. This is what goes on happening to each mind — you long for something, it will happen, but by the time it happens you will see that you are still discontent. Something else is creating the misery now.</p>
</blockquote>
<p>You seek something, you achieve it, and then you seek again — until you become a Buddha.</p>

