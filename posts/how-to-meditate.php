<h2>セットアップ</h2>
<p>僕の瞑想環境はシンプルです：</p>
<ul>
  <li>ヨガマット（床に敷く）</li>
  <li>瞑想用クッション（ザフ）</li>
  <li>ブランケット（膝や肩に掛ける）</li>
</ul>
<p>壁際の静かなスペースに常設しています。毎朝ここに座るだけ。</p>

<h2>なぜ朝一番にやるのか</h2>
<p>瞑想は朝起きたら最初にやることが大切です。</p>
<p>理由は、脳がまだ覚醒状態に入っていないから。交感神経が活性化しておらず、副交感神経が優位な状態にいる。この状態だと瞑想に入りやすい。</p>
<p>朝食を食べたり、コーヒーを飲んだり、スマホを見た後だと、交感神経が上がってしまい、瞑想の難易度が跳ね上がります。</p>

<h2>やり方</h2>
<ul>
  <li>毎朝起きたら最初にやる（朝食前、スマホ前）</li>
  <li>約30分</li>
  <li>クッションに座り、目を閉じる</li>
  <li>呼吸に意識を向ける</li>
</ul>

<p>呼吸はコントロールしなくていい。ただ吸って、吐く。それをゆっくりやるだけ。</p>

<h3>考えが浮かんできたら</h3>
<p>瞑想中、必ず何か考えが浮かんできます。</p>
<p>それを否定しない。「あ、また浮かんじゃったな」と気づいて、また呼吸に戻す。吸って、吐いて、吸って、吐いて。それだけ。</p>
<p>最初はありえないくらい難しいです。息苦しくなったりもする。でも徐々に慣れてきます。</p>

<h2>3年かかった</h2>
<p>ぶっちゃけ、めちゃくちゃ時間がかかりました。</p>
<p>3年間くらい継続して、やっと「瞑想で交感神経を下げて、心拍数を落として、体をリラックス状態に持っていく」というアクションを、いつでも実行できるようになった。</p>
<p>3年は長い。でも一度できるようになると、これがめちゃくちゃ便利。</p>

<h2>不眠症が治った</h2>
<p>僕は元不眠症で、10年くらい悩んでいました。</p>
<p>布団に入っても3時間、4時間寝れない。その間ずっと読書をしていて、どんなにつまらない本を読んでも全然眠れなかった。</p>
<p>しかし瞑想を覚えてからは、寝た瞬間に瞑想を始める。</p>
<p>瞑想すると何も考えない状態になる。体が瞑想状態に入り、深く深く意識が落ちていく。すると気づいたら、いつの間にか寝ている。</p>
<p>最近は30分〜1時間くらいで寝落ちできるようになりました。タイマー（大麻）を使えば5〜10分で寝落ちできる。量は1回あたり0.3g〜0.5gを吸うだけ。</p>
<p>10年間の不眠症が、瞑想で解決した。これが一番大きかった。</p>

