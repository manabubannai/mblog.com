<h3>手順②：腹式呼吸して落ち着く</h3>

<p>吸いつつ腹を膨らませ、吐きつつ腹を凹ませる。<br>
基本的にはこれだけで、次の効果があります。</p>

<ul>
  <li>ストレスや不安の軽減</li>
  <li>睡眠の質が向上する</li>
  <li>体幹が安定していく</li>
  <li>体全体のリラックス</li>
  <li>酸素吸収がアップする</li>
</ul>

<p>寝る前にも使えます。</p>

<p>１つコツがあります。</p>

<ul>
  <li>４秒吸いつつ、腹を膨らませる</li>
  <li>４秒止める</li>
  <li>６秒吐きつつ、腹を凹ませる</li>
  <li>２秒止める</li>
</ul>

<p>これを「4-4-6-2 呼吸法」と呼びます。<br>
秒数は「アプリ」で測ると簡単です。<br>
<a href="https://pacedbreathing.com/" target="_blank">https://pacedbreathing.com/</a></p>

